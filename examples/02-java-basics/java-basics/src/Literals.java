public class Literals {
	
	  public static void main(String[] args) {
		  
		  boolean result = true;
		  System.out.println(result);
		  
		  int numberInDec = 16;
		  int numberInOct= 020;
		  int numberInHex = 0x10;
		  System.out.println(numberInDec);
		  System.out.println(numberInOct);
		  System.out.println(numberInHex);
		  
		  long longInt = 2344L;
		  System.out.println(longInt);
		  
		  float realNumber = 12.5f;
		  System.out.println(realNumber);
		  
		  realNumber = 1.25e+2f;
		  System.out.println(realNumber);
		  
		  double d = 12.5d;
		  System.out.println(d);
		  
		  double dd = 12.5;
		  System.out.println(dd);
		  
		  char symbolOne = 'a';
		  System.out.println(symbolOne);
		  
		  char symbolTwo = '\u0061';
		  System.out.println(symbolTwo);
		  
		  String str = "Peter\'s car!";
		  System.out.println(str);
	  }
}
