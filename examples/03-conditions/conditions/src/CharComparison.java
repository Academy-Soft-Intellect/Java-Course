public class CharComparison {
	
	 public static void main(String[] args) {
		 
		 char c = 'c';
		 System.out.println(c);
		 
		 char a = 'a';
		 System.out.println(a);
		 
		 if (a > c) {
			 System.out.println("a > c");
		 }
		 else {
			 System.out.println("a <= c");
		 }
		 
		 System.out.println((int)a);
		 System.out.println((int)c);
	}
}
